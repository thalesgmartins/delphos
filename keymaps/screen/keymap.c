    static uint8_t last_layer = 255;

    void render_layer_icon(void) {
        uint8_t current_layer = get_highest_layer(layer_state);

        if (current_layer != last_layer) {
            oled_clear();
            oled_set_cursor(0, -10);

            switch (current_layer) {
                case _DEFAULT:
					oled_write_raw_P(bitmap_Layer_1, sizeof(bitmap_Layer_1));
                    //oled_write("Default", false);
                    break;
                case _TECNICON:
                    oled_write_raw_P(bitmap_Layer_2, sizeof(bitmap_Layer_2));
                    //oled_write("Tecnicon", false);
                    break;
                case _TEST:
                	oled_write_raw_P(bitmap_Layer_3, sizeof(bitmap_Layer_3));
                	//oled_write("Teste", false);
                	break;
                case _DEBUGGING:
                	oled_write_raw_P(bitmap_Layer_4, sizeof(bitmap_Layer_4));
                	//oled_write("Debbuging", false);
                    break;
            }

            last_layer = current_layer;
        }
    }

